# Pide al usuario que introduzca 3 calificaciones, y calcule la media de estas.
num_1 = float(input("Introduce un numero: "))
num_2 = float(input("Introduce otro numero: "))
num_3 = float(input("Introduce otro numero: "))

media = (num_1 + num_2 + num_3) / 3
print("La media es: ", media)
